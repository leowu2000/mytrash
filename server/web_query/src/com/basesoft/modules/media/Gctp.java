package com.basesoft.modules.media;

public class Gctp {

	public String id;
	public String name;
	public String note;
	public String gclj;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getNote() {
		return note;
	}
	
	public void setNote(String note) {
		this.note = note;
	}

	public String getGclj() {
		return gclj;
	}

	public void setGclj(String gclj) {
		this.gclj = gclj;
	}
}
